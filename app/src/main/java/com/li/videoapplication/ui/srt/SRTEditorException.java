
package com.li.videoapplication.ui.srt;
/**
 * 编辑字幕异常
 */
public class SRTEditorException extends SRTException {
    private static final long serialVersionUID = 1L;

 
    public SRTEditorException(String message, Throwable cause) {
        super(message, cause);
    }

 
    public SRTEditorException(String message) {
        super(message);
    }


    public SRTEditorException(Throwable cause) {
        super(cause);
    }
}
